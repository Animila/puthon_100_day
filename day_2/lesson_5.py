# не округляется
print(8 / 3)

# показывает только целый (не изменяется)
print(int(8 / 3))
print(8 // 3)

# округляется до целых
print(round(8/3))

# округляется до 2 знаков после запятой
print(round(8 / 3, 2))

# сокращения
count = 0
count += 1

# добавление других данных в строку
print(f'Score - {count}')